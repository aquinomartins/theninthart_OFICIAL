CREATE TABLE quadrants (
    id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    stable_key VARCHAR(100) NOT NULL,
    number INT UNSIGNED NOT NULL,
    position INT UNSIGNED NOT NULL,
    block_key VARCHAR(100) NOT NULL,
    block_label VARCHAR(255) NOT NULL,
    fixed_function VARCHAR(255) NOT NULL,
    narrative_purpose TEXT NOT NULL,
    aspect_class VARCHAR(100) NOT NULL,
    grid_area VARCHAR(100) NOT NULL,
    continuity_group VARCHAR(100) NOT NULL,
    narrative_weight DECIMAL(6,3) NOT NULL DEFAULT 1.000,
    previous_quadrant_key VARCHAR(100) NULL,
    next_quadrant_key VARCHAR(100) NULL,
    gutter_before_key VARCHAR(100) NULL,
    gutter_after_key VARCHAR(100) NULL,
    affected_controls_json LONGTEXT NOT NULL,
    affected_widgets_json LONGTEXT NOT NULL,
    allowed_version_ids_json LONGTEXT NOT NULL,
    image_required TINYINT(1) NOT NULL DEFAULT 1,
    caption_allowed TINYINT(1) NOT NULL DEFAULT 1,
    dialogue_allowed TINYINT(1) NOT NULL DEFAULT 1,
