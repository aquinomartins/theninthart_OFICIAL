CREATE TABLE story_controls (
    id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    stable_key VARCHAR(100) NOT NULL,
    position INT UNSIGNED NOT NULL,
    family_key VARCHAR(100) NOT NULL,
    family_label VARCHAR(255) NOT NULL,
    title VARCHAR(255) NOT NULL,
    short_title VARCHAR(255) NOT NULL,
    description TEXT NOT NULL,
    control_type VARCHAR(50) NOT NULL,
    default_value TINYINT(1) NOT NULL DEFAULT 0,
    enabled TINYINT(1) NOT NULL DEFAULT 1,
    affected_quadrants_json LONGTEXT NOT NULL,
    tags_json LONGTEXT NOT NULL,
    narrative_axes_json LONGTEXT NOT NULL,
    conflicts_json LONGTEXT NOT NULL,
    synergies_json LONGTEXT NOT NULL,
    metadata_json LONGTEXT NOT NULL,
    manifest_version VARCHAR(50) NOT NULL,
    created_at DATETIME NOT NULL,
    updated_at DATETIME NOT NULL,
    PRIMARY KEY (id),
    UNIQUE KEY uq_story_controls_stable_key (stable_key),
    UNIQUE KEY uq_story_controls_position (position)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

