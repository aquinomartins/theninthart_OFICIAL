    UNIQUE KEY uq_quadrants_position (position)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE quadrant_variants (
    id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    slot_key VARCHAR(100) NOT NULL,
    quadrant_id BIGINT UNSIGNED NOT NULL,
    story_version_id BIGINT UNSIGNED NOT NULL,
    quadrant_number INT UNSIGNED NOT NULL,
    version_number INT UNSIGNED NOT NULL,
    position INT UNSIGNED NOT NULL,
    title VARCHAR(255) NOT NULL,
    description TEXT NOT NULL,
    narrative_payload_json LONGTEXT NOT NULL,
    tags_json LONGTEXT NOT NULL,
    required_controls_json LONGTEXT NOT NULL,
    excluded_controls_json LONGTEXT NOT NULL,
    widget_constraints_json LONGTEXT NOT NULL,
    status VARCHAR(50) NOT NULL,
    revision INT UNSIGNED NOT NULL DEFAULT 1,
    metadata_json LONGTEXT NOT NULL,
    created_at DATETIME NOT NULL,
    updated_at DATETIME NOT NULL,
    PRIMARY KEY (id),
    UNIQUE KEY uq_quadrant_variants_slot_key (slot_key),
    UNIQUE KEY uq_quadrant_variants_quadrant_version (quadrant_id, story_version_id),
    UNIQUE KEY uq_quadrant_variants_position (position),
    CONSTRAINT fk_quadrant_variants_quadrant FOREIGN KEY (quadrant_id) REFERENCES quadrants (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT fk_quadrant_variants_story_version FOREIGN KEY (story_version_id) REFERENCES story_versions (id) ON UPDATE CASCADE ON DELETE RESTRICT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE media_assets (
    id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    quadrant_variant_id BIGINT UNSIGNED NOT NULL,
    asset_path VARCHAR(500) NULL,
    expected_path VARCHAR(500) NOT NULL,
    mime_type VARCHAR(100) NULL,
    width INT UNSIGNED NULL,
    height INT UNSIGNED NULL,
    aspect_ratio VARCHAR(50) NULL,
    alt_text VARCHAR(500) NOT NULL,
    caption TEXT NULL,
    status VARCHAR(50) NOT NULL,
    checksum CHAR(64) NULL,
    revision INT UNSIGNED NOT NULL DEFAULT 1,
    metadata_json LONGTEXT NOT NULL,
    created_at DATETIME NOT NULL,
    updated_at DATETIME NOT NULL,
    PRIMARY KEY (id),
    UNIQUE KEY uq_media_assets_quadrant_variant_id (quadrant_variant_id),
    KEY idx_media_assets_expected_path (expected_path),
    CONSTRAINT fk_media_assets_quadrant_variant FOREIGN KEY (quadrant_variant_id) REFERENCES quadrant_variants (id) ON UPDATE CASCADE ON DELETE RESTRICT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
